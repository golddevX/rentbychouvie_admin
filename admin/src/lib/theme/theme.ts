export type ThemeMode = 'light' | 'dark';

export type ThemeKey =
  | 'ocean'
  | 'emerald'
  | 'violet'
  | 'rose'
  | 'amber'
  | 'zhengGold';

export type ResolvedThemeKey =
  | 'ocean-light'
  | 'ocean-dark'
  | 'emerald-light'
  | 'emerald-dark'
  | 'violet-light'
  | 'violet-dark'
  | 'rose-light'
  | 'rose-dark'
  | 'amber-light'
  | 'amber-dark'
  | 'zhengGold-light'
  | 'zhengGold-dark';

export type ThemePreset = {
  key: ResolvedThemeKey;
  base: ThemeKey;
  mode: ThemeMode;
  label: string;
  description: string;
  preview: {
    accent: string;
    surface: string;
    text: string;
    gradient: string;
  };
  tokens: Record<string, string>;
};

type ThemeTokens = {
  '--surface': string;
  '--surface-1': string;
  '--surface-2': string;
  '--surface-3': string;
  '--surface-4': string;
  '--surface-5': string;
  '--surface-border': string;
  '--text-primary': string;
  '--text-secondary': string;
  '--text-muted': string;
  '--accent-solid': string;
  '--accent-strong': string;
  '--accent-from': string;
  '--accent-via': string;
  '--accent-to': string;
  '--overlay': string;
  '--success': string;
  '--warning': string;
  '--danger': string;
  '--info': string;
  '--input-bg': string;
  '--input-border': string;
  '--input-text': string;
  '--input-placeholder': string;
  '--input-focus-ring': string;
  '--button-primary-bg': string;
  '--button-primary-text': string;
  '--button-primary-border': string;
  '--button-primary-hover': string;
  '--button-secondary-bg': string;
  '--button-secondary-border': string;
  '--button-secondary-hover': string;
  '--button-ghost-hover': string;
  '--button-danger-bg': string;
  '--button-danger-border': string;
  '--button-danger-hover': string;
  '--button-danger-text': string;
  '--chip-bg': string;
  '--chip-border': string;
  '--chip-text': string;
};

function makePreset({
  key,
  base,
  mode,
  label,
  description,
  tokens,
}: {
  key: ResolvedThemeKey;
  base: ThemeKey;
  mode: ThemeMode;
  label: string;
  description: string;
  tokens: ThemeTokens;
}): ThemePreset {
  return {
    key,
    base,
    mode,
    label,
    description,
    preview: {
      accent: tokens['--accent-solid'],
      surface: tokens['--surface-2'],
      text: tokens['--text-primary'],
      gradient: `linear-gradient(135deg, rgb(${tokens['--accent-from']}), rgb(${tokens['--accent-via']}), rgb(${tokens['--accent-to']}))`,
    },
    tokens,
  };
}

